package com.embraceplus.model;

public class ThemeObj {
private String themeName;
private int themeOrder;
private String themeIcon;
private String themeBackground;
private boolean isSelected;

public String getThemeName() {
	return themeName;
}

public void setThemeName(String themeName) {
	this.themeName = themeName;
}

public int getThemeOrder() {
	return themeOrder;
}

public void setThemeOrder(int themeOrder) {
	this.themeOrder = themeOrder;
}

public String getThemeIcon() {
	return themeIcon;
}

public void setThemeIcon(String themeIcon) {
	this.themeIcon = themeIcon;
}

public String getThemeBackground() {
	return themeBackground;
}

public void setThemeBackground(String themeBackground) {
	this.themeBackground = themeBackground;
}

public boolean isSelected() {
	return isSelected;
}

public void setSelected(boolean isSelected) {
	this.isSelected = isSelected;
}


}
