package com.embraceplus.model;

public class ExCommandObj {
	String exCommandName;
	String msgIcon;
	boolean selected;
	public String getExCommandName() {
		return exCommandName;
	}
	public void setExCommandName(String exCommandName) {
		this.exCommandName = exCommandName;
	}
	public boolean isSelected() {
		return selected;
	}
	public void setSelected(boolean selected) {
		this.selected = selected;
	}
	public String getMsgIcon() {
		return msgIcon;
	}
	public void setMsgIcon(String msgIcon) {
		this.msgIcon = msgIcon;
	}

}
